#include "fillit.h"

void	isprinttab(char **tab)
{
	int i;
	int j;

	i = 0;
	while (tab[i][0] != '$')
	{
		j = 0;
		while (tab[i][j] != '$')
			ft_putchar(tab[i][j++]);
		ft_putchar('\n');
		i++;
	}
}
