#include "fillit.h"

int		iscontact(char *str)
{
	int i;
	int cntr;

	i = 0;
	cntr = 0;
	while (i < 20)
	{
		if (str[i] == '#')
		{
			if (i - 5 >= 0 && str[i - 5] == '#')
				cntr++;
			if (i - 1 >= 0 && str[i - 1] == '#')
				cntr++;
			if (i + 1 < 20 && str[i + 1] == '#')
				cntr++;
			if (i + 5 < 20 && str[i + 5] == '#')
				cntr++;
		}
		i++;
	}
	if (cntr == 6 || cntr == 8)
		return (1);
	return (0);
}
