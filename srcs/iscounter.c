#include "fillit.h"

int		iscounter(char *str, char c)
{
	int i;
	int cntr;

	cntr = 0;
	i = 0;
	while (str[i])
	{
		if (str[i] == c)
			cntr++;
		i++;
	}
	return (cntr);
}
